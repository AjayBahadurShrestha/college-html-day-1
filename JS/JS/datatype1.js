// Non Primary Data Types
// 1. Object
// 2. Array
// 3. Function

// Object - key: value pair
let student={
  name:"Ajay Bahadur Shrestha",
  age: 20,
  faculty: "BScIT"
}
console.log(student)
console.log(typeof student)  
console.log(student.name) //accessing keys

// 2. Array - 
let fruits = ["apple", "mango", "banana", 12]
console.log(fruits)
console.log(typeof fruits)


// 3. Function - reusable block of code.
function greet(){
  console.log("Hello World!");
}
greet()
console.log(typeof greet)